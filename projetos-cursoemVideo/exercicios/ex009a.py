#Faça um programa que leia um número Inteiro qualquer e mostre na tela a sua tabuada.
n = int(input('Digite um Numero '))
i = 0
print(f'\n---a tabuada do {n} (com While)---')

while i <= 10:
    resultado = n*i
    print(f'{n} x {i} = {resultado}')
    i += 1

print('-' * 15)



    
    
    
  
    

   


